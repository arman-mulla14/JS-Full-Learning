const pi = 3.14; 


if(pi == "3.14"){
    console.log("Yes"); 
}else{
    console.log("Not Pi")
}


if(pi === 3.14){
    console.log("Yes"); 
}else{
    console.log("Not Pi")
}


if(pi != 3.14){
    console.log("Yes"); 
}else{
    console.log("Not Pi")
}

if(pi !== 3.14){
    console.log("Yes"); 
}else{
    console.log("Not Pi")
}


