let arr =    [1, 2, 3, 4, 5]; 
// using toString() to convert the array to a string
// that will convert the array to a string, and it will return the string representation of the array without modifying the original array
let str = arr.toString(); 
console.log(str); // Output: "1,2,3,4,5"

console.log(typeof str); // Output: "string" (since the result of toString() is a string    )