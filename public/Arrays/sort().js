let arr = [1, 2,3,7,8,9,0,5,4,3,2,35,2, 3, 4, 5];
// using sort() to sort the elements of the array
// that will sort the elements of the array in ascending order by default, and it will modify the original array
arr.sort();
console.log(arr); // Output: [0, 1, 2, 2, 2, 3, 3, 3, 4, 4, 5, 5, 7, 8, 9, 35]