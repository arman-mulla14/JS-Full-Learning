let arr = [1, 2, 3, 4, 5];
// using  array.of() to create a new array from a list of arguments
// that will create a new array from a list of arguments, and it will return a new array without modifying the original array
let newArr = Array.of(1, 2, 3, 4, 5);
console.log(newArr); // Output: [1, 2, 3, 4, 5]
console.log(arr); // Output: [1, 2, 3, 4, 5] (original array is not modified)
