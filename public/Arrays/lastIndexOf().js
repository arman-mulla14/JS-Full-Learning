let roll= [1, 2, 3, 4, 5, 3];   

// using lastIndexOf() to find the last index of an element in the array
// that will return the index of the last occurrence of the element in the array, and -1 if the element is not found in the array
console.log(roll.lastIndexOf(2)); // Output: 1
console.log(roll.lastIndexOf(3)); // Output: 5